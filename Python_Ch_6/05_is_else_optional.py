a = 6
if(a==7):
    print("Yes")
elif(a>56):
    print("no and yes")
else:
    print("I am optional")

# The 'else' block is optional in Python.
# If no 'else' block is provided, the program will simply continue executing the next lines of code after the 'if' or 'elif' blocks.
# This means that if the condition in the 'if' or 'elif' statement is not met, the program will not execute any specific code for that condition.
# However, if you want to handle cases where none of the conditions are met, you can use an 'else' block.
# In this case, the 'else' block is optional, and the program will continue executing without it.
# In the above code, if 'a' is not equal to 7 and not greater than 56, it will print "I am optional".
# If 'a' is equal to 7, it will print "Yes".
# If 'a' is greater than 56, it will print "no and yes".
# If 'a' is neither equal to 7 nor greater than 56, it will print "I am optional".
# This allows for more flexible control flow in your program.
