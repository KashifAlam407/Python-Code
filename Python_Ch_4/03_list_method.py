l1 = [1, 8, 7, 2, 21, 15]
print(l1)
l1.sort()  ## sort the list
print(l1)
l1.reverse()  ## reverses the list
print(l1)
l1.append(45)   ## appends (add) at the end of the list
print(l1)
l1.insert(2, 43)   ## insert 43 at the index of 2 in the list
print(l1)
l1.pop(2)    ## delete the value of index no. 2
print(l1)
l1.remove(8)   ## remove 8 (first occurence) from the list 
print(l1)



