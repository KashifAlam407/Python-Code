# Creatig a tuple using ()
t = (1, 2, 4, 5, 32, 12)

# t1 = ()   # Empty tuple 
# t1 = (1)   # Wrong way to declare a Tuple with Single element
t1 = (1,)   # Tuple with Single element
print(t1)

# printing the element of a tuple through indexing
print(t[0]) 

# cannot update the values of a tuple
t[0] = 8
print(t)